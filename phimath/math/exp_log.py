import math
# -------------------------------------------------
# Exponential
# -------------------------------------------------

def exp(x: float) -> float:
    return math.exp(x)
# -------------------------------------------------
# Natural logarithm
# -------------------------------------------------

def ln(x: float) -> float:
    return math.log(x)

# -------------------------------------------------
# Logarithm with arbitrary base
# -------------------------------------------------

def log(x: float, base: float = 10.0) -> float:
    return math.log(x, base)

